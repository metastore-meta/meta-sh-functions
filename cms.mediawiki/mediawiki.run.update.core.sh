# -------------------------------------------------------------------------------------------------------------------- #
# Script: Settings.
# -------------------------------------------------------------------------------------------------------------------- #

source "${HOME}/.apps/mediawiki.settings.sh"

# -------------------------------------------------------------------------------------------------------------------- #
# -----------------------------------------------------< SCRIPT >----------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Update.
# -------------------------------------------------------------------------------------------------------------------- #

function mw.run.update.core() {
	for domain in ${domains[@]}; do
		HTTP_HOST="${domain}"
		export HTTP_HOST
		/usr/bin/php "${mw_path}/maintenance/update.php" --quick --skip-external-dependencies
	done
}

mw.run.update.core

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Exit.
# -------------------------------------------------------------------------------------------------------------------- #

exit 0
