#!/usr/bin/env bash

# -------------------------------------------------------------------------------------------------------------------- #
# WordPress. Run CRON.
# -------------------------------------------------------------------------------------------------------------------- #
# @author Kitsune Solar <https://kitsune.solar>
# @version 1.0.0
# -------------------------------------------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Settings.
# -------------------------------------------------------------------------------------------------------------------- #

source "${HOME}/.apps/wordpress.settings.sh"

# -------------------------------------------------------------------------------------------------------------------- #
# -----------------------------------------------------< SCRIPT >----------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Update.
# -------------------------------------------------------------------------------------------------------------------- #

cd ${wp_path}

for domain in ${domains[@]}; do
	HTTP_HOST="${domain}"
	export HTTP_HOST
	"${HOME}/bin/wp" cron event run --due-now
done

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Exit.
# -------------------------------------------------------------------------------------------------------------------- #

exit 0
