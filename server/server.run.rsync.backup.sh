#!/usr/bin/env bash

# -------------------------------------------------------------------------------------------------------------------- #
# rSync Backup Script
# -------------------------------------------------------------------------------------------------------------------- #
# @author Kitsune Solar <https://kitsune.solar>
# @version 1.0.0
# -------------------------------------------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Settings.
# -------------------------------------------------------------------------------------------------------------------- #

ssh_user=""
ssh_pass=""
ssh_host=""
ssh_port=""

dir_local=""
dit_remote=""

# -------------------------------------------------------------------------------------------------------------------- #
# -----------------------------------------------------< SCRIPT >----------------------------------------------------- #
# -------------------------------------------------------------------------------------------------------------------- #

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Backup data.
# -------------------------------------------------------------------------------------------------------------------- #

sshpass -p "${ssh_pass}" rsync -avze "ssh -p ${ssh_port}" ${ssh_user}@${ssh_host}:${dit_remote} ${dir_local}

# -------------------------------------------------------------------------------------------------------------------- #
# Script: Exit.
# -------------------------------------------------------------------------------------------------------------------- #

exit 0
